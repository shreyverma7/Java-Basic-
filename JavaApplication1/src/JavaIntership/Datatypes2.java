
package JavaIntership;


public class Datatypes2 {
    //Primitive datatypes
    public static void main(String args[]){
        int a =4 ;
        byte b = 7;
        short c = 00;
        long d = 17491441;
        float e = 1232.343f;
        double f = 425.1546356;
        char g = 'r';
        String h = "shrey is a good boy ";// non primitive
        int i[] = {12,134,433,23};//non primitive
      
        System.out.println(a);//print integer
        System.out.println(b);//print byte data type
        System.out.println(c);//print short
        System.out.println(d);// print long data type
        System.out.println(e);// print float
        System.out.println(f);// print double data type
        System.out.println(g);// print character
        System.out.println(h);// print string
        System.out.println(i[0]);// array's first position     start from 0 adressing
        System.out.println(i[1]);// array's second  
        System.out.println(i[2]);// array's third positin
        
    }
    
    
    
    
}
